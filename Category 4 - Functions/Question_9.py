# Return the largest item from the given list



def higher_number(list):
    high = (max(list))
    print(f"The higher number of the given list is {high}.")



higher_number([4, 6, 8, 24, 12, 2])
higher_number([80, 52, 63, 99, 10])
higher_number([1, 2, 3, 4])

